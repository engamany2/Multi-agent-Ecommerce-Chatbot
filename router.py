# --- Memory Storage Management ---
store = {} # In-memory dictionary to hold history for each user session

def get_session_history(session_id: str) -> BaseChatMessageHistory:
    if session_id not in store:
        store[session_id] = ChatMessageHistory()
    return store[session_id]

# 3. Initialize the base chain and wrap it with Memory support
base_router_chain = create_router()

class RouterWrapper:
    # كلمات مفتاحية مضمونة — لو لقيناها نوجه من غير ما نسأل الـ LLM
    FAQ_KEYWORDS = [
        'توصيل', 'شحن', 'بتوصلوا', 'بتشحنوا', 'delivery', 'shipping',
        'استرجاع', 'ارجاع', 'ارجعه', 'ارجع', 'استبدال', 'return', 'refund',
        'ضمان', 'كفالة', 'warranty',
        'تقسيط', 'اقسط', 'أقساط', 'installment',
        'سياسة', 'policy',
        'اتصل', 'واتساب', 'whatsapp', 'ايميل', 'email', 'رقم المحل',
        'خدمة العملاء', 'شكوى', 'support'
    ]
    PRODUCT_KEYWORDS = [
        'سماعات', 'سماعة', 'موبايل', 'تليفون', 'تليفونات', 'ساعات', 'ساعة',
        'لابتوب', 'شاشة', 'ماوس', 'كيبورد', 'باور بانك', 'شاحن',
        'ايفون', 'آيفون', 'سامسونج', 'أبل', 'سوني', 'شاومي', 'ون بلس',
        'headphone', 'phone', 'watch', 'laptop', 'iphone', 'samsung', 'apple',
        'سعر', 'كام', 'عندكوا', 'عندكم', 'ايه عندكم', 'متاح', 'انواع',
        'sony', 'beats', 'jbl', 'airpods', 'galaxy'
    ]
    ORDER_KEYWORDS = ['فين الاوردر', 'طلبي', 'شحنتي', 'رقم الطلب', 'tracking', 'order status']
    CHITCHAT_ONLY = ['مرحبا', 'أهلا', 'اهلا', 'شكرا', 'hello', 'hi', 'thanks', 'bye', 'هاى', 'هاي', 'السلام عليكم']

    def _keyword_route(self, text):
        """Deterministic keyword routing — 100% accurate for clear cases."""
        text_lower = text.lower().strip()
        
        # Check FAQ first (delivery, returns, warranty, contact)
        for kw in self.FAQ_KEYWORDS:
            if kw in text_lower:
                return "faq_agent"
        
        # Check order tracking
        for kw in self.ORDER_KEYWORDS:
            if kw in text_lower:
                return "order_tracking"
        
        # Check product search
        for kw in self.PRODUCT_KEYWORDS:
            if kw in text_lower:
                return "product_search"
        
        # Check if it's PURE chitchat (no product words)
        if text_lower in self.CHITCHAT_ONLY or (len(text_lower) < 15 and any(kw == text_lower for kw in self.CHITCHAT_ONLY)):
            return "chitchat"
        
        return None  # Ambiguous — let LLM decide

    def invoke(self, inputs, config=None):
        session_id = config.get("configurable", {}).get("session_id", "default")
        user_input = inputs["input"]
        
        # 1. Try keyword-based routing first (deterministic, 100% accurate)
        keyword_result = self._keyword_route(user_input)
        
        if keyword_result:
            # Create a result object matching the expected format
            result = type('Route', (), {'destination': keyword_result})()
        else:
            # 2. Fallback to LLM for ambiguous cases
            result = base_router_chain.invoke({"input": user_input})
        
        # Save user message to session history
        history = get_session_history(session_id)
        history.add_user_message(user_input)
        return result

router_with_memory = RouterWrapper()

print("[SUCCESS] Full Router System with Memory & Llama 3.3 is ready!")



