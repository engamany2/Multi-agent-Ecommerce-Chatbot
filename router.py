# 4. Function to test the router with session support
def ask_router_with_memory(user_input, session_id="default_user"):
    try:
        # Pass the session_id to the config
        config = {"configurable": {"session_id": session_id}}

        # Invoke the memory-enabled chain
        response = router_with_memory.invoke(
            {"input": user_input},
            config=config
        )

        decision = response.destination.upper()

        # Handle specific visual feedback for different decisions
        icon = "➡️"
        if decision == "CHITCHAT": icon = "👋"
        if decision == "OUT_OF_SCOPE": icon = "❌"

        print(f"--- Router Test (Session: {session_id}) ---")
        print(f"User: {user_input}")
        print(f"Decision: {icon} {decision}")
        print(f"----------------------\n")

    except Exception as e:
        print(f"Oops! Something went wrong: {e}")



